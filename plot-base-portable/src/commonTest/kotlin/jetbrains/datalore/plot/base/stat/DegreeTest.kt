    fun rss(p: PolynomialFunction, xs: List<Double>, ys: List<Double>): Double {
        var res = 0.0

        for ((x, y) in xs.asSequence().zip(ys.asSequence())) {
            res += (y - p.value(x)).pow(2)
        }

        return res
    }

    fun testError(p: PolynomialFunction, xs: List<Double>, ys: List<Double>): Double {
        var res = 0.0

        for ((x, y) in xs.asSequence().zip(ys.asSequence())) {
            res += abs((y - p.value(x)))
        }

        return res
    }

    fun testError2(p: PolynomialFunction, xs: List<Double>, ys: List<Double>): Double {
        var res = 0.0

        for ((x, y) in xs.asSequence().zip(ys.asSequence())) {
            res += (y - p.value(x)).pow(2)
        }

        return res
    }

